let fruits = ["apple", "orange" , "Kiwi","mango"];
console.log("..................Forward Order..............")
for(let i=0;i<=fruits.length-1;i++){
    console.log(fruits[i]);
}

console.log("---------------------------------------");

console.log("...............Reverse Order............")
for(let i=fruits.length-1;i>=0;i--){
    console.log(fruits[i]);
}